
#Operators are used to perform operations on variables and values.

x = 5
y = 3
print(x + y)


#   +	Addition	    x + y	
#   -	Subtraction 	x - y	
#   *	Multiplication	x * y	
#   /	Division	    x / y	
#   %	Modulus     	x % y	
#   **	Exponentiation	x ** y	
#   //	Floor division	x // y

x = 5
x += 3
print(x)

#Operator	Example	        Same As
#    =	    x = 5	        x = 5	
#    +=	    x += 3	        x = x + 3	
#   -=	    x -= 3	        x = x - 3	
#   *=	    x *= 3	        x = x * 3	
#    /=	    x /= 3	        x = x / 3	
#    %=	    x %= 3	        x = x % 3	
#   //=	    x //= 3	        x = x // 3	
#   **=	    x **= 3	        x = x ** 3	
#    &=	    x &= 3	        x = x & 3	
#   |=	    x |= 3	        x = x | 3	
#   ^=	    x ^= 3	        x = x ^ 3	
#   >>=	    x >>= 3	        x = x >> 3	
#   <<=	    x <<= 3	        x = x << 3